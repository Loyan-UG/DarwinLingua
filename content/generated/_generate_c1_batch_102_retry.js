const fs = require('fs');
const path = require('path');
const cp = require('child_process');
const root='D:/_Projects/DarwinLingua', level='C1', levelLower='c1', batch='102-retry';
const sourcePath=path.join(root,'content',`${level}.txt`);
const taxonomyPath=path.join(root,'content','taxonomy','darwinlingua-taxonomy-v1.json');
const basePath=path.join(root,'content','generated','de-c1-generated-batch-102.json');
const outPath=path.join(root,'content','generated',`de-${levelLower}-generated-batch-${batch}.json`);
const importProject=path.join(root,'src','Apps','DarwinLingua.ImportTool','DarwinLingua.ImportTool.csproj');
const expected=['synthetisieren','die Systematik','systematisch','die Systematisierung','die Teilhabegerechtigkeit','thematisieren'];
const taxonomy=JSON.parse(fs.readFileSync(taxonomyPath,'utf8'));
const labelMap=new Map((taxonomy.labels||[]).map(l=>[l.key,l]));
const base=JSON.parse(fs.readFileSync(basePath,'utf8'));
const entry=base.entries.find(e=>e.word==='thematisieren');
if(!entry) throw new Error('Missing thematisieren entry');
entry.usageLabels=['formal','business','written'];
const usedLabels=['formal','business','written'];
const labels=usedLabels.map(k=>{const l=labelMap.get(k); if(!l) throw new Error(`Missing label ${k}`); return l;});
const pkg={packageVersion:'1.0',packageId:`de-${levelLower}-generated-batch-${batch}`,packageName:`German ${level} Generated Batch 102 Retry`,source:'Hybrid',defaultMeaningLanguages:base.defaultMeaningLanguages,labels,entries:[entry],collections:[],scenarios:[],conversationStarterPacks:[],eventPreparationPacks:[]};
fs.writeFileSync(outPath,JSON.stringify(pkg,null,2),'utf8');
const result=cp.spawnSync(`dotnet run --project "${importProject}" -- --target shared --yes "${outPath}"`,{shell:true,cwd:root,encoding:'utf8',maxBuffer:1024*1024*10});
const output=`${result.stdout||''}${result.stderr||''}`;
const imported=(output.match(/Entries imported:\s*(\d+)/)||[])[1];
const invalid=(output.match(/Entries invalid:\s*(\d+)/)||[])[1];
const warnings=(output.match(/Warnings:\s*(\d+)/)||[])[1];
const ok=result.status===0&&imported==='1'&&invalid==='0'&&warnings==='0';
const tokens=fs.readFileSync(sourcePath,'utf8').split(',').map(s=>s.trim()).filter(Boolean);
let removed=false;
if(ok){const remaining=tokens.filter(t=>!expected.includes(t)); if(tokens.length-remaining.length!==expected.length) throw new Error('Exact delete count mismatch'); fs.writeFileSync(sourcePath,remaining.join(', '),'utf8'); removed=true;}
const finalTokens=fs.readFileSync(sourcePath,'utf8').split(',').map(s=>s.trim()).filter(Boolean);
console.log(JSON.stringify({sourcePath,words:expected,outPath,status:result.status,imported,invalid,warnings,removed,remainingCount:finalTokens.length,first10:finalTokens.slice(0,10),importOutputTail:output.split(/\r?\n/).filter(l=>/Entries imported|Entries invalid|Warnings|Import completed|Import failed|Issues|Error|Warning/i.test(l)).slice(-20)},null,2));
process.exit(ok?0:2);
