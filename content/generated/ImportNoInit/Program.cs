using System.Text.Json;
using DarwinLingua.ContentOps.Application.Abstractions;
using DarwinLingua.ContentOps.Application.Models;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Logging;

if (args.Length != 1) throw new InvalidOperationException("Pass exactly one JSON file path.");
string root = @"D:\_Projects\DarwinLingua";
string settingsPath = Path.Combine(root, @"src\Apps\DarwinLingua.WebApi\appsettings.Development.json");
using FileStream stream = File.OpenRead(settingsPath);
using JsonDocument doc = JsonDocument.Parse(stream);
string connection = doc.RootElement.GetProperty("ConnectionStrings").GetProperty("SharedCatalogAdmin").GetString() ?? throw new InvalidOperationException("Missing connection.");
ServiceCollection services = new();
services.AddLogging(builder => builder.SetMinimumLevel(LogLevel.Warning));
DarwinLingua.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddDarwinLinguaInfrastructureForPostgres(services, connection);
DarwinLingua.Catalog.Application.DependencyInjection.ServiceCollectionExtensions.AddCatalogApplication(services);
DarwinLingua.Catalog.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddCatalogInfrastructure(services);
DarwinLingua.ContentOps.Application.DependencyInjection.ServiceCollectionExtensions.AddContentOpsApplication(services);
DarwinLingua.ContentOps.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddContentOpsInfrastructure(services);
DarwinLingua.Learning.Application.DependencyInjection.ServiceCollectionExtensions.AddLearningApplication(services);
DarwinLingua.Learning.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddLearningInfrastructure(services);
DarwinLingua.Localization.Application.DependencyInjection.ServiceCollectionExtensions.AddLocalizationApplication(services);
DarwinLingua.Localization.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddLocalizationInfrastructure(services);
DarwinLingua.Practice.Application.DependencyInjection.ServiceCollectionExtensions.AddPracticeApplication(services);
DarwinLingua.Practice.Infrastructure.DependencyInjection.ServiceCollectionExtensions.AddPracticeInfrastructure(services);
await using ServiceProvider provider = services.BuildServiceProvider();
IContentImportService svc = provider.GetRequiredService<IContentImportService>();
ImportContentPackageResult result = await svc.ImportAsync(new ImportContentPackageRequest(Path.GetFullPath(args[0])), CancellationToken.None);
foreach (string lemma in result.ImportedLemmas) Console.WriteLine(lemma);
Console.WriteLine("Import summary");
Console.WriteLine($"Entries total: {result.TotalEntries}");
Console.WriteLine($"Entries imported: {result.ImportedEntries}");
Console.WriteLine($"Entries skipped as duplicates: {result.SkippedDuplicateEntries}");
Console.WriteLine($"Entries invalid: {result.InvalidEntries}");
Console.WriteLine($"Warnings: {result.WarningCount}");
foreach (ImportIssueModel issue in result.Issues) Console.WriteLine($"{issue.Severity}: {issue.Message}");
Environment.ExitCode = result.IsSuccess ? 0 : 2;
